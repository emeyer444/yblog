<h2>The <i>yBlog.php</i> File</h2>
<p>In the page template, the <code>REQUIRE_ONCE&nbsp;./lib/yBlog.php</code> statement 
calls the maikn script to generate the page head, nav bar, responsive body header, TOC, 
plugin insertion, and footer. This file need not be edited, but you can change the 
top-of-page layout and other such features in this file. </p>

<h2>The <i>config.php</i> File</h2>
<p><li>The <i>./lib/yBlog.php</i> file first calls a small site variable file, <i>./lib/config.php</i>. 
The <i>./lib/config.php</i> file sets the site's name, base URL, and options. This file then generates  
one set of variables for the public server, and another for localhost at http://127.0.0.1 
which is selected automatically, depending where the directory resides. This file also sets site options, such 
as the logo, default stylesheet, and ID's for Google and Adobe integration. Additionally it provides
default page values for any variables not set on individual content pages.</p>
