<p>The <i>./lib/yBlog.php</i> file then generates all the metadata for the page's HEAD element 
from the page and site variables. It also includes the site stylesheet 
 and all  scripts needed for Google Analytics and plugins. </p>
<p>The <i>./lib/yBlog.php</i> file then closes the HEAD element and adds an opening &lt;BODY> tag.
At the top of the BODY element, yBlog inserts a HEADER element which contains the responsive header. 
A NAV element inside the HEADER element containing the site menu. The menu contents 
generated automatically. </p>
<p>The <i>./lib/head.php</i> file then parses the page and replaces the PHP plugin snippets with 
HTML andJavaScript, generated by the <i>./lib/plugins.php</i> file.</p> 

<h2>The <i>foot.php</i> File</h2>
<p>After an article's content, the <i>./lib/yBlog.php</i> file adds footer content from the 
<i>./lib/foot.php</i>file. Each blog site will prefer a different footer, which is edited just 
like a standard Web page. The <i>.lib/yBlog.php file</i> then performs 
any necessary parsing on the whole page and sends it to the browser client.</p>